import streamlit as st
import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns

st.set_page_config(page_title="Financial Risk Analyzer", layout="centered")
st.title("ğŸ“‰ Financial Risk Analyzer")
st.markdown("Analyze earnings manipulation (Beneish M-Score) and bankruptcy risk (Altman Zâ€²-Score).")

# File upload
uploaded_file = st.file_uploader("ğŸ“‚ Upload your processed dataset (CSV)", type="csv")

if uploaded_file:
    df = pd.read_csv(uploaded_file)

    # Validate required columns
    required_cols = {'is_fraud', 'z_score', 'bankruptcy_risk'}
    if not required_cols.issubset(df.columns):
        st.error(f"âŒ Your dataset must contain the following columns: {', '.join(required_cols)}")
    else:
        st.success("âœ… File successfully loaded!")

        # Show data preview
        with st.expander("ğŸ” View raw data"):
            st.dataframe(df.head())

        # Summary table
        st.subheader("ğŸ“Š Fraud vs. Bankruptcy Risk Counts")
        summary = df.groupby(['is_fraud', 'bankruptcy_risk']).size().reset_index(name='count')
        st.dataframe(summary)

        # Visualization
        st.subheader("ğŸ“ˆ Risk Distribution Chart")
        fig, ax = plt.subplots(figsize=(8, 5))
        sns.countplot(data=df, x='bankruptcy_risk', hue='is_fraud', palette='Set2', ax=ax)
        ax.set_title('Fraud Risk vs. Bankruptcy Risk')
        ax.set_xlabel("Bankruptcy Risk Category")
        ax.set_ylabel("Number of Companies")
        ax.legend(title="Fraudulent (Beneish M-Score)", labels=["No", "Yes"])
        st.pyplot(fig)

        # Z-score statistics
        st.subheader("ğŸ“ Altman Zâ€²-Score Summary")
        stats = df.groupby('is_fraud')['z_score'].agg(['mean', 'median', 'std', 'count']).rename(
            index={0: 'Non-Fraud', 1: 'Fraud'}
        )
        st.dataframe(stats)

else:
    st.info("ğŸ‘† Upload a CSV to begin.")